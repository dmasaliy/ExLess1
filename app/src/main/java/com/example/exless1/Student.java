package com.example.exless1;

public class Student {

    private String name;
    private int group;
    private String photo;

    public Student(String name, int group, String photo) {
        this.name = name;
        this.group = group;
        this.photo = photo;
    }

    public String getName() {
        return name;
    }

    public int getGroup() {
        return group;
    }

    public String getPhoto() {
        return photo;
    }




}
